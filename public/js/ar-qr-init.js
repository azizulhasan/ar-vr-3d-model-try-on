/**
 * AtlasAR — QR code initializer.
 *
 * Replaces the inline <script> that used to be emitted by
 * AR_TRY_ON_Helper::get_qr_code(). PHP now outputs only a
 * <div id="atlas_ar_qr_code" data-atlas-qr-url="…" data-atlas-qr-brand="…">
 * placeholder, and this enqueued file builds the QR into it. Keeping the
 * logic in a real file (instead of an inline <script>) lets the PHP output
 * be escaped with wp_kses() and removes the EscapeOutput phpcs:ignore.
 *
 * Depends on `ar-try-on-qr-generator.min.js`, which exposes window.qrcode.
 * That library is deferred, so it may not be ready the first time this
 * runs — poll up to ~5s on a 100ms interval and bail cleanly.
 */
(function () {
	'use strict';

	function buildQr() {
		var el = document.getElementById( 'atlas_ar_qr_code' );
		if ( ! el || el.getAttribute( 'data-atlas-qr-done' ) === '1' ) {
			return;
		}

		var url   = el.getAttribute( 'data-atlas-qr-url' ) || window.location.href;
		var brand = el.getAttribute( 'data-atlas-qr-brand' ) || '';

		var typeNumber           = 0;
		var errorCorrectionLevel = 'L';
		var tries                = 0;
		var maxTries             = 50;

		var interval = setInterval( function () {
			tries++;

			if ( window.qrcode ) {
				clearInterval( interval );

				var qr = window.qrcode( typeNumber, errorCorrectionLevel );
				qr.addData( url );
				qr.make();

				// Close button.
				var closeBtn = document.createElement( 'button' );
				closeBtn.id        = 'ar_close_btn';
				closeBtn.innerHTML = '&times;';

				// QR image markup is generated by the qrcode library (a
				// base64 data-URI <img>), not from user input.
				var imgWrap       = document.createElement( 'span' );
				imgWrap.innerHTML = qr.createImgTag();

				el.innerHTML = '';
				el.appendChild( closeBtn );
				el.appendChild( imgWrap );

				// Brand label — set via textContent so the value can never
				// inject markup.
				if ( brand ) {
					var brandDiv       = document.createElement( 'div' );
					brandDiv.className = 'atlas_ar_qr_brand';
					brandDiv.textContent = brand;
					el.appendChild( brandDiv );
				}

				closeBtn.addEventListener( 'click', function () {
					el.style.display = 'none';
				} );

				el.setAttribute( 'data-atlas-qr-done', '1' );
			} else if ( tries >= maxTries ) {
				clearInterval( interval );
			}
		}, 100 );
	}

	if ( document.readyState === 'loading' ) {
		document.addEventListener( 'DOMContentLoaded', buildQr );
	} else {
		buildQr();
	}
})();
